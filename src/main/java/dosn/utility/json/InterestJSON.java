package dosn.utility.json;

public class InterestJSON {

	private String interest;

	public InterestJSON() {
		super();
	}

	public InterestJSON(String interest) {
		super();
		this.interest = interest;
	}

	public String getInterest() {
		return interest;
	}

	public void setInterest(String interest) {
		this.interest = interest;
	}

}
